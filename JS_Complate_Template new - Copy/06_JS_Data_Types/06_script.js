// number example
var age = 25;
console.log("age :" + age + "Type of a :" + typeof age);


// String Examples
var greeting = 'Good Morning';
console.log("greeting :" + greeting + 'datatype :' + typeof greeting);



// boolean Examples
var isJSEasy = true;
console.log("isJSEasy :" + isJSEasy + 'datatype :' + typeof isJSEasy);



// reassignment example of variables
var xyz;
console.log('xyz :' + xyz);

xyz = 10;
console.log('xyz :' + xyz);

xyz = 'text';
console.log('xyz :' + xyz);



// Undefined Example
var somevar;
console.log("somevar :" + somevar + " datatype :" + typeof somevar);



// null example
var abc = null;
console.log("abc :" + abc + "datatype :" + abc);

